package entity;

public class LeYouJiaCommunity {
    
    private String communityName;//小区名字
    private String rentNumber;//在租房屋数量
    private String saleNumber;//在卖房屋数量
    private String buildYear;//建造年代
    private String liveNumber;//规划户数
    private String buildArea;//建筑面积
    private String plotRatio;//容积率
    private String numberOfParkingSpaces;//车位个数
    private String afforestationRate;//绿化率
    private String propertyCompany;//物业公司
    
    public String getCommunityName() {
        return communityName;
    }
    public void setCommunityName(String communityName) {
        this.communityName = communityName;
    }
    public String getRentNumber() {
        return rentNumber;
    }
    public void setRentNumber(String rentNumber) {
        this.rentNumber = rentNumber;
    }
    public String getSaleNumber() {
        return saleNumber;
    }
    public void setSaleNumber(String saleNumber) {
        this.saleNumber = saleNumber;
    }
    public String getBuildYear() {
        return buildYear;
    }
    public void setBuildYear(String buildYear) {
        this.buildYear = buildYear;
    }
    public String getLiveNumber() {
        return liveNumber;
    }
    public void setLiveNumber(String liveNumber) {
        this.liveNumber = liveNumber;
    }
    public String getBuildArea() {
        return buildArea;
    }
    public void setBuildArea(String buildArea) {
        this.buildArea = buildArea;
    }
    public String getPlotRatio() {
        return plotRatio;
    }
    public void setPlotRatio(String plotRatio) {
        this.plotRatio = plotRatio;
    }
    public String getNumberOfParkingSpaces() {
        return numberOfParkingSpaces;
    }
    public void setNumberOfParkingSpaces(String numberOfParkingSpaces) {
        this.numberOfParkingSpaces = numberOfParkingSpaces;
    }
    public String getAfforestationRate() {
        return afforestationRate;
    }
    public void setAfforestationRate(String afforestationRate) {
        this.afforestationRate = afforestationRate;
    }
    public String getPropertyCompany() {
        return propertyCompany;
    }
    public void setPropertyCompany(String propertyCompany) {
        this.propertyCompany = propertyCompany;
    }
    
    
}
